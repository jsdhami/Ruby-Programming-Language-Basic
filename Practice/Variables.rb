# In Ruby, there are four types of variables 
# 1. Local Variables
# 2. Instance Variables
# 3. Class Variables
# 4. Global Variables
# #Local Variable
# Local variables are the variables that are defined in a method.
# Local variables are not available outside the method.
# You will see more details about method in subsequent chapter.
# Local variable names must begin with a lowercase letter or _.
# If you begin a local variable name with a capital letter, Ruby will think that it is a constant and hence can parse the literal value to that variable as a value, instead of considering it as a variable name.
# Local variables do not have any default value, which means local variables cannot be used until they have been initialized.
